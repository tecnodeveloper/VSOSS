//! program in C++ to find the perfect numbers between 1 and 500.
#include<iostream>
using namespace std;
int main()
{
    int i{1} , n{0},sum{0};
    cout<<"Input integer: ";
    cin>>n;
    while (i <= n)
    {
        if(n%i == 0)
        {
            sum += i;
        }
        i++;
    }
        if(sum == i)
        {
            cout<<"Perfect number";
        }
        else
        cout<<"Not a perfect number";
    return 0;
}

